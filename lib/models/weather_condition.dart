enum WeatherCondition {
  sunny,          // Clear sunny weather
  still,          // Clear weather at night
  sunWithClouds,  // clouds cover 25% or more
  cloudy,         // Overcast or cloudy weather
  rainy,          // Rainy weather
  snowy,          // Snowfall
  thunderstorm,   // Thunderstorms
  windy,          // Windy conditions
  foggy,          // Foggy or misty weather
  drizzle,        // Light rain or drizzle
  hail,           // Hailstorm
  tornado,        // Tornado
  hurricane,      // Hurricane or tropical storm
  unknown,        // Unknown or unspecified weather condition
}